"""
Report Builder Module.

Compiles registered metadata, validation summaries, and collected metrics 
into standardized JSON and Markdown research artifacts.
"""

import json
import dataclasses
from pathlib import Path
from typing import Any, Dict

from framework.evaluation.experiment_registry import ExperimentRecord
from framework.evaluation.metrics_summary import ExperimentMetrics


class ReportBuilder:
    """Generates structured experiment reports."""

    def __init__(
        self,
        record: ExperimentRecord,
        metrics: ExperimentMetrics,
        validation_summary: Dict[str, bool],
        output_dir: Path
    ):
        self.record = record
        self.metrics = metrics
        self.validation_summary = validation_summary
        self.output_dir = output_dir
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def _compile_payload(self) -> Dict[str, Any]:
        """Assembles the unified data dictionary."""
        return {
            "metadata": dataclasses.asdict(self.record),
            "validation": self.validation_summary,
            "metrics": dataclasses.asdict(self.metrics)
        }

    def generate_json(self) -> Path:
        """Exports the payload as a JSON file."""
        payload = self._compile_payload()
        report_path = self.output_dir / f"{self.record.experiment_id}_report.json"
        
        with open(report_path, "w", encoding="utf-8") as f:
            json.dump(payload, f, indent=4)
            
        return report_path

    def generate_markdown(self) -> Path:
        """Exports the payload as a human-readable Markdown file."""
        report_path = self.output_dir / f"{self.record.experiment_id}_report.md"
        payload = self._compile_payload()
        
        lines = [
            f"# Experiment Report: {self.record.experiment_id}",
            f"**Version:** {self.record.version} | **Timestamp:** {self.record.timestamp}",
            "---",
            "## 1. Environment Identity",
            f"* **OS:** {self.record.os_info}",
            f"* **Python:** {self.record.python_version}",
            f"* **Configuration Hash:** `{self.record.config_hash}`",
            "",
            "## 2. Pre-Flight Validation",
        ]
        
        for check, status in payload["validation"].items():
            icon = "✅ PASS" if status else "❌ FAIL"
            lines.append(f"* **{check}**: {icon}")
            
        lines.extend([
            "",
            "## 3. Execution Metrics",
            f"* **Status:** {self.metrics.status}",
            f"* **Duration:** {self.metrics.execution_duration_sec:.2f} seconds",
            f"* **Latency:** {self.metrics.latency_ms:.2f} ms",
            f"* **Throughput:** {self.metrics.throughput_mbps:.2f} MB/s",
            "",
            "### Packet Counts",
        ])
        
        for key, count in self.metrics.packet_counts.items():
            lines.append(f"* **{key}**: {count}")
            
        lines.extend(["", "---", "*Generated by VPNResearchLab Evaluation Harness*"])
        
        report_path.write_text("\n".join(lines), encoding="utf-8")
        return report_path